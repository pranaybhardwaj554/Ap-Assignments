public class company {
    private double res_charges;
    private double dev_charges;

    public company(double res_charges, double dev_charges) {
        this.res_charges = res_charges;
        this.dev_charges = dev_charges;
    }

    public double getRes_charges() {
        return res_charges;
    }

    public void setRes_charges(double res_charges) {
        this.res_charges = res_charges;
    }

    public double getDev_charges() {
        return dev_charges;
    }

    public void setDev_charges(double dev_charges) {
        this.dev_charges = dev_charges;
    }
}
